print("\nOPERACIONES ARITMÉTICAS")

a = int(input("Ingrese el primer número para operar: "))
b = int(input("Ingrese el segundo número para operar: "))

suma = a + b
resta = a-b
multiplicacion = a*b
division = a/b
divisionR = round(division,2)

print("\nEl resultado de la suma es:",suma)
print("El resultado de la resta es:",resta)
print("El resultado de la multiplicación es:",multiplicacion)
print("El resultado de la división es:",divisionR)